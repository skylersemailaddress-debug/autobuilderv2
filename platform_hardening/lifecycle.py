MAINTENANCE_SEMANTICS: dict[str, object] = {
    "long_term_maintenance_contract": "v1",
    "scaffold_ownership": "autobuilder_generated",
    "operator_customization_policy": "operator_owned_after_generation",
    "update_strategy": "regenerate_with_merge_review",
    "breaking_change_policy": "require_explicit_migration_contract",
    "deprecation_notice_required": True,
    "lts_lane_designation_required": False,
    "supported_lifecycle_operations": [
        "initial_generation",
        "capability_addition",
        "capability_removal",
        "schema_migration",
        "stack_upgrade",
        "lane_transition",
    ],
    "blocked_lifecycle_operations": [
        "silent_destructive_overwrite",
        "lane_downgrade_without_contract",
    ],
}


def build_lifecycle_contract(lane_id: str) -> dict[str, object]:
    """Build a full lifecycle contract for a lane, including regen + maintenance semantics."""
    return {
        "lane_id": lane_id,
        "lifecycle_contract_version": "v1",
        "maintenance_semantics": MAINTENANCE_SEMANTICS,
        "regen_safety_levels": REGEN_SAFETY_LEVELS,
        "supported_lifecycle_operations": MAINTENANCE_SEMANTICS["supported_lifecycle_operations"],
        "blocked_lifecycle_operations": MAINTENANCE_SEMANTICS["blocked_lifecycle_operations"],
        "migration_contract_available": True,
        "capability_evolution_tracking": True,
        "maturity": "bounded_prototype",
        "known_limitations": [
            "lifecycle tooling is scaffold-contract only; execution requires operator CI integration",
            "migration scripts are not auto-generated from schema diffs",
        ],
    }
